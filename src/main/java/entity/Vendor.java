package entity;

/**
 *
 * @author ductr
 */
public class Vendor {

    private int id;
    private String name;
    private String origin;

    public Vendor() {
    }

    public Vendor(String name, String origin) {
        this.name = name;
        this.origin = origin;
    }

    public String getOrigin() {
        return origin;
    }

    public void setOrigin(String origin) {
        this.origin = origin;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
