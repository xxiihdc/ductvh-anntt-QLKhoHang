    private void setLanguage() {
        String lang = XLanguage.language;
        if (lang.equalsIgnoreCase("vi")) {
            bundlePath = "config.toolbar1-vi";
            resourceBundle = ResourceBundle.getBundle(bundlePath, new Locale("vi-VN"));
            return;
        }
        bundlePath = "config.toolbar1-en";
        resourceBundle = ResourceBundle.getBundle(bundlePath, new Locale("en-EN"));
        gBtnLogout.setText(resourceBundle.getString("gBtnLogout"));
        gBtnSetting.setText(resourceBundle.getString("gBtnSetting"));
        gBtnUser.setText(resourceBundle.getString("gBtnUser"));
    }

