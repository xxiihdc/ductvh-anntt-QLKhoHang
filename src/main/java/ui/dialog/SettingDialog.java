    private boolean readConfig() {
        try {
            FileReader reader = new FileReader("src\\main\\resources\\config\\config.properties");
            properties.load(reader);
            String scols = properties.getProperty("cols");
            String srow = properties.getProperty("row");
            String snumber = properties.getProperty("number");
            if (!scols.equals("0") && !srow.equals("0") && !snumber.equals("0")) {
                cols = Integer.parseInt(scols);
                row = Integer.parseInt(srow);
                num = Integer.parseInt(snumber);
                txtCols.setText(cols + "");
                txtRow.setText(row + "");
                txtNum.setText(num + "");
                String num = properties.getProperty("soLuong");
                txtSoLuong.setText(num);
                String date = properties.getProperty("date");
                txtDate.setText(date);
                String lang = XLanguage.language;
                if (lang.equalsIgnoreCase("vi")) {
                    cbxLang.setSelectedIndex(0);
                } else {
                    cbxLang.setSelectedIndex(1);
                }
                String theme = properties.getProperty("theme");
                if (theme.equalsIgnoreCase("dark")) {
                    cbxTheme.setSelectedIndex(1);
                } else {
                    cbxTheme.setSelectedIndex(0);
                }
                reader.close();
                return true;
            } else {
                return false;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            return false;
        }
    }

    private boolean valid() {
        String num = txtCols.getText() + txtNum.getText() + txtRow.getText() + txtSoLuong.getText();
        for (int i = 0; i < num.length(); i++) {
            if (!Character.isDigit(num.charAt(i))) {
                return false;
            }
        }
        int number = Integer.parseInt(txtNum.getText());
        int cols = Integer.parseInt(txtCols.getText());
        int row = Integer.parseInt(txtRow.getText());
        if (number <= cols * row) {
            return true;
        } else {
            return false;
        }

    }
