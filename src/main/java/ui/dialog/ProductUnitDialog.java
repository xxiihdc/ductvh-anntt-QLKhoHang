    private boolean valid() {
        String msg = "";
        String reID = "[a-zA-Z0-9]{1,7}";
        if(!txtID.getText().matches(reID)){
           msg += "Mã DVT chỉ bao gồm chữ cái và số, dưới 7 ký tự";
        }
          if(txtName.getText().length()==0) msg += "\n Tên DVT không được rỗng";
          if(msg.length()==0)return true;
          MsgBox.alert(null, msg);
          return false;
        
    }

    private void update() {
        MsgBox.alert(null, "Đang thực hiện");
    }

    private void clearForm() {
        setForm(new ProductUnit());
    }

    private void insert() {
        dao.insert(getForm());
    }

    private ProductUnit getForm() {
        return new ProductUnit(txtID.getText(),txtName.getText(),txtNote.getText());
    }

    private void setForm(ProductUnit p) {
        txtID.setText(p.getId());
        txtName.setText(p.getName());
        txtNote.setText(p.getDescription());
    }
