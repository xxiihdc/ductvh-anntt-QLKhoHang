    private void fillTable() {
        ProductBatchDAO dao= new ProductBatchDAO(); 
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        model.setRowCount(0);
        List<ProductBatch> lst = dao.selectUnsorted();
        for (ProductBatch s : lst) {
            Object[] data = new Object[]{
                s.getId(),
                s.getProductID(), s.getEnteredDate(),s.getUnsortedProduct()
            };
            model.addRow(data);
        }
        //lblRecord.setText("0/" + tblProductGroup.getRowCount());
    }
