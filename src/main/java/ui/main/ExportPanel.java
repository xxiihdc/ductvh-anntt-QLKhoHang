    private void fillTable() {
        ExportDAO dao = new ExportDAO();
        List<Export> lst;
        Date from = txtDateFrom.getDate();
        Date to = txtDateTo.getDate();
        if(from == null && to == null){
          lst = dao.selectAll();
        }else{
            String d1s,d2s;
            if(from != null) d1s = Xdate.toString(from, "yyyy-MM-dd");
            else d1s = "1990-01-01";
            if(to != null) d2s = Xdate.toString(to, "yyyy-MM-dd");
            else d2s = Xdate.toString(new Date(), "yyyy-MM-dd");
            lst = dao.selectByDate(d1s,d2s);
        }
        
        DefaultTableModel model = (DefaultTableModel) tblExport.getModel();
        model.setRowCount(0);
         
        for (Export s : lst) {
            Object[] row = new Object[]{
                s.getId(),s.getStaffId(),
                s.getTotalProduct(),s.getTotalQuantity(),
                Xdate.toString(s.getDate(),"dd-MM-yyyy")
                
            };
            model.addRow(row);
        }
        lblRecord.setText("0/" + tblExport.getRowCount());
    }
