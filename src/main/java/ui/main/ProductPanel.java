    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        model.setRowCount(0);
        List<Product> lst;
        boolean stop = jCheckBox1.isSelected();
        if(stop) lst = dao.selectStop();
        else lst = dao.selectAll();
        for (Product s : lst) {
            Object[] row = new Object[]{
                s.getId(), s.getName(), s.getGroupName(), s.getProductTypeName(), s.getProductUnitID(),
                s.isStatus() ? "Đang KD" : "Ngừng KD"
            };
            model.addRow(row);
        }
        lblRecord.setText("0/" + tblProduct.getRowCount());
    }

    private void fillTableByKey(String key) {
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        model.setRowCount(0);
        List<Product> lst = dao.selectByKey(key);
        for (Product s : lst) {
            Object[] row = new Object[]{
                s.getId(), s.getName(), s.getGroupName(), s.getProductTypeName(), s.getProductUnitID(),
                s.isStatus() ? "Đang KD" : "Ngừng KD"
            };
            model.addRow(row);
        }
        lblRecord.setText("0/" + tblProduct.getRowCount());
    }

    private boolean defaultPos() {
        int i = tblProduct.getSelectedRow();
        if (i == -1) {
            tblProduct.setRowSelectionInterval(0, 0);
            lblRecord.setText("1/" + tblProduct.getRowCount());
            return true;
        }
        return false;
    }

    private void next() {
        if (defaultPos()) {
            return;
        }
        int pos = tblProduct.getSelectedRow() + 1;
        if (pos == tblProduct.getRowCount()) {
            tblProduct.setRowSelectionInterval(0, 0);
            lblRecord.setText("1/" + tblProduct.getRowCount());
            return;
        }
        tblProduct.setRowSelectionInterval(0, pos);
        lblRecord.setText((pos+1) + "/" + tblProduct.getRowCount());

    }

    private void prev() {
        if (defaultPos()) {
            return;
        }
        int pos = tblProduct.getSelectedRow() - 1;
        if (pos == -1) {
            tblProduct.setRowSelectionInterval(0, tblProduct.getRowCount() - 1);
            lblRecord.setText(tblProduct.getRowCount() + "/" + tblProduct.getRowCount());
            return;
        }
        tblProduct.setRowSelectionInterval(0, pos);
        lblRecord.setText((pos+1) + "/" + tblProduct.getRowCount());

    }

    private void last() {
        if (defaultPos()) {
            return;
        }
        tblProduct.setRowSelectionInterval(0, tblProduct.getRowCount() - 1);
        lblRecord.setText(tblProduct.getRowCount() + "/" + tblProduct.getRowCount());

    }

    private void first() {
       if (defaultPos()) {
            return;
        }
        tblProduct.setRowSelectionInterval(0, 0);
        lblRecord.setText(1 + "/" + tblProduct.getRowCount());
    }

    private void clearForm() {
        txtSearch.setText("");
        fillTable();
        jCheckBox1.setSelected(false);
    }
