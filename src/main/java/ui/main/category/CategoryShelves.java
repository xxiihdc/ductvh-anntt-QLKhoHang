    private void fillTable() {
        ShelvesDAO dao = new ShelvesDAO();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        List<Shelves> lst = dao.selectAll();
        for (Shelves s : lst) {
            Object[] row = new Object[]{
                s.getId(),s.isStatus() ? "Đang hoạt động" : "Dừng hoạt động",
                s.getC().getRGB()
            };
            model.addRow(row);
        }
        lblRecord.setText("0/" + jTable1.getRowCount());
    }
